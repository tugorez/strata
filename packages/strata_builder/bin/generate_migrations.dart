#!/usr/bin/env dart

import 'dart:io';
import 'package:path/path.dart' as p;

/// Generates migrations.g.dart from SQL migration files.
///
/// This script scans the migrations/ directory and generates a Dart file
/// containing all migrations as SqlMigration objects.
void main(List<String> arguments) async {
  final migrationsDir = arguments.isNotEmpty ? arguments[0] : 'migrations';
  final outputFile = arguments.length > 1
      ? arguments[1]
      : 'lib/migrations.g.dart';

  print('🔍 Scanning for migrations in $migrationsDir/...');

  final dir = Directory(migrationsDir);
  if (!dir.existsSync()) {
    stderr.writeln('❌ Error: Directory $migrationsDir does not exist');
    exit(1);
  }

  // Find all .sql files
  final sqlFiles =
      dir
          .listSync()
          .whereType<File>()
          .where((f) => f.path.endsWith('.sql'))
          .toList()
        ..sort((a, b) => p.basename(a.path).compareTo(p.basename(b.path)));

  if (sqlFiles.isEmpty) {
    stderr.writeln('❌ No migration files found in $migrationsDir/');
    exit(1);
  }

  print('✓ Found ${sqlFiles.length} migration file(s)');

  // Generate the Dart code
  final buffer = StringBuffer();
  buffer.writeln('// GENERATED CODE - DO NOT MODIFY BY HAND');
  buffer.writeln('// Generated by bin/generate_migrations.dart');
  buffer.writeln();
  buffer.writeln("import 'package:strata_sqlite/strata_sqlite.dart';");
  buffer.writeln();
  buffer.writeln(
    '/// Auto-generated migrations from the migrations/ directory.',
  );
  buffer.writeln('class Migrations {');
  buffer.writeln('  static List<SqlMigration> get all => [');

  for (final file in sqlFiles) {
    final fileName = p.basename(file.path);

    // Extract timestamp from filename
    final timestampMatch = RegExp(r'^(\d{14})_').firstMatch(fileName);
    if (timestampMatch == null) {
      print(
        '⚠️  Skipping $fileName (invalid format, expected YYYYMMDDHHmmss_name.sql)',
      );
      continue;
    }

    final timestampStr = timestampMatch.group(1)!;
    final version = int.parse(timestampStr);
    final name = fileName
        .replaceFirst(RegExp(r'^\d{14}_'), '')
        .replaceFirst('.sql', '');

    // Parse the SQL file
    final content = await file.readAsString();
    final sections = _parseMigrationFile(content);
    final upStatements = sections['up'] ?? [];
    final downStatements = sections['down'];

    if (upStatements.isEmpty) {
      print('⚠️  Warning: $fileName has no up statements');
    }

    buffer.writeln('        SqlMigration(');
    buffer.writeln('          version: $version,');
    buffer.writeln('          name: ${_escapeString(name)},');
    buffer.writeln('          upStatements: [');
    for (final statement in upStatements) {
      buffer.writeln('            ${_escapeString(statement)},');
    }
    buffer.writeln('          ],');

    if (downStatements != null && downStatements.isNotEmpty) {
      buffer.writeln('          downStatements: [');
      for (final statement in downStatements) {
        buffer.writeln('            ${_escapeString(statement)},');
      }
      buffer.writeln('          ],');
    }

    buffer.writeln('        ),');

    print('  ✓ $fileName');
  }

  buffer.writeln('      ];');
  buffer.writeln('}');

  // Write the output file
  final output = File(outputFile);
  output.parent.createSync(recursive: true);
  await output.writeAsString(buffer.toString());

  print('');
  print('✨ Generated $outputFile');
}

/// Parses a migration SQL file and extracts up/down statements.
Map<String, List<String>?> _parseMigrationFile(String content) {
  final lines = content.split('\n');
  final upStatements = <String>[];
  List<String>? downStatements;

  String? currentSection; // 'up' or 'down'
  final buffer = StringBuffer();

  for (var line in lines) {
    final trimmed = line.trim();

    // Check for section markers
    if (RegExp(r'--\s*@Up\(\)', caseSensitive: false).hasMatch(trimmed)) {
      // Save any buffered content from previous section
      if (buffer.isNotEmpty) {
        _addStatement(
          currentSection,
          buffer.toString(),
          upStatements,
          downStatements,
        );
        buffer.clear();
      }
      currentSection = 'up';
      continue;
    } else if (RegExp(
      r'--\s*@Down\(\)',
      caseSensitive: false,
    ).hasMatch(trimmed)) {
      // Save any buffered content from UP section
      if (buffer.isNotEmpty) {
        _addStatement(
          currentSection,
          buffer.toString(),
          upStatements,
          downStatements,
        );
        buffer.clear();
      }
      currentSection = 'down';
      downStatements ??= [];
      continue;
    }

    // Skip comment-only lines and empty lines
    if (trimmed.isEmpty || trimmed.startsWith('--')) {
      continue;
    }

    // If we haven't hit a marker yet, default to 'up'
    currentSection ??= 'up';

    // Append the line to the buffer
    buffer.writeln(line);

    // If the line ends with a semicolon, it's the end of a statement
    if (trimmed.endsWith(';')) {
      _addStatement(
        currentSection,
        buffer.toString(),
        upStatements,
        downStatements,
      );
      buffer.clear();
    }
  }

  // Add any remaining buffered content
  if (buffer.isNotEmpty) {
    _addStatement(
      currentSection,
      buffer.toString(),
      upStatements,
      downStatements,
    );
  }

  return {'up': upStatements, 'down': downStatements};
}

void _addStatement(
  String? section,
  String statement,
  List<String> upStatements,
  List<String>? downStatements,
) {
  final trimmed = statement.trim();
  if (trimmed.isEmpty) return;

  if (section == 'up') {
    upStatements.add(trimmed);
  } else if (section == 'down' && downStatements != null) {
    downStatements.add(trimmed);
  }
}

/// Escapes a string for use in generated Dart code.
String _escapeString(String str) {
  // Escape backslashes first, then single quotes, then dollar signs, then newlines
  final escaped = str
      .replaceAll(r'\', r'\\')
      .replaceAll("'", r"\'")
      .replaceAll(r'$', r'\$')
      .replaceAll('\n', r'\n')
      .replaceAll('\r', r'\r')
      .replaceAll('\t', r'\t');
  return "'$escaped'";
}
